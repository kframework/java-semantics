public class op_013_int_to_int_postfix {
  public static void main(String[] args) {
    int a = 10;
    System.out.println("a   = " + a);
    System.out.println("a++ = " + (a++));
    System.out.println("a   = " + a);
    System.out.println("a-- = " + (a--));
    System.out.println("a   = " + a);
    System.out.println("Done!");
  }
}
